package com.hiresight.hiresightclient;

public class Client {
    private String name, regNo, icNo, contact, email, password, imageURL;

    public Client(){

    }

    public Client(String name, String regNo, String icNo, String contact, String email, String password, String imageURL) {
        this.name = name;
        this.regNo = regNo;
        this.icNo = icNo;
        this.contact = contact;
        this.email = email;
        this.password = password;
        this.imageURL = imageURL;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRegNo() {
        return regNo;
    }

    public void setRegNo(String regNo) {
        this.regNo = regNo;
    }

    public String getIcNo() {
        return icNo;
    }

    public void setIcNo(String icNo) {
        this.icNo = icNo;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getImageURL() {
        return imageURL;
    }

    public void setImageURL(String imageURL) {
        this.imageURL = imageURL;
    }
}

